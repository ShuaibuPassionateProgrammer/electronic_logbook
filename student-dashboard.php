                        <span style="color: red;font-size: 15px;">*</span>   To prepare students for the work situation they are likely to meet after graduation.<br>
                        <span style="color: red;font-size: 15px;">*</span>   To expose students to work methods and techniques in handling equipment and machineries that they may not be available in the universities.<br>
