                        <p style="padding-left:15px;text-align: justify;font-family: Segoe UI;">Students Industrial Work Experience Scheme (SIWES) is one of the industrial training fund (ITF) program. It was designed to give Nigerian students studying occupationally related courses in higher institutions the experience that would supplement their theoretical learning. SIWES unit in the University Maiduguri was established in the year 1986 as an industrial coordinating unit catering for mostly science and some art based students on industrial attachment. The foundation staff were four (4) that comprised an industrial coordinator who is responsible to the Vice Chancellor for the day to day running of the affair of the office, a secretary/typist and an office assistant. SIWES unit was finally and officially commissioned on July, 2006.</p>

